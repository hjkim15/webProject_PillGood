package project;

public class pharmacistBean {
	private String userId;
	private int career;
	private int pCode;
	
	
	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserId() {
		return this.userId;
	}

	public void setCareer(int career) {
		this.career = career;
	}

	public int getCareer() {
		return this.career;
	}

	public void setPCode(int pCode) {
		this.pCode = pCode;
	}

	public int getPCode() {
		return this.pCode;
	}
}
